using UnityEngine;

public class Manager_DDOL : MonoBehaviour
{
    public static Manager_DDOL Instance;

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
        }
        else
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
    }
}
